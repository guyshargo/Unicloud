def getWordCount(stemTerms, url):
   # Fetch the page
    url, soup = fetch_page(url)

    #page was fetched successfully
    if soup is None:
        return 0

    # Extract the words
    idx = index_words(soup)
    idx = stemming(idx)

    # Count the words in the query
    words_count = 0
    for stemmed_term in stemTerms:
        words_count += idx.get(stemmed_term, 0)
    return words_count

